package com.company;

public class Math {

    public static int addition(int first, int second){
        return first+second;
    }

    public static int subtraction(int first, int second){
        return first-second;
    }

    public static int multiplication(int first, int second){
        return first*second;
    }

    public static int division(int first, int second){
        return first/second;
    }
}
